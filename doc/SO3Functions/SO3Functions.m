%% Rotational Functions
%
% The |SO3Fun| toolbox is a collection of Matlab classes for numerical 
% computations with functions on the rotation group $\mathcal{SO}(3)$.
% It overloads the default commands for vectors and matrices to compute 
% the analogous operations for functions of the given type.
% The underlying mathematical approach is accomplished via Wigner-D functions 
% which form an orthonormal basis of the square-integrable functions on the 
% rotation group.
%
